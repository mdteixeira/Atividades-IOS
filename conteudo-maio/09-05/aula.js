// Case Switch

var cor = 5;

switch (cor) {
    case 1:
        cor = 'vermelho';
        break;
    case 2:
        cor = 'azul';
        break;
    case 3:
        cor = 'verde';
        break;
    case 4:
        cor = 'cinza';
        break;
    case 5:
        cor = 'amarelo';
}

console.log(cor);

